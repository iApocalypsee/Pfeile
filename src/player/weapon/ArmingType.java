package player.weapon;

/**
 * The type of weapon or armour value.
 * <p>Cutting</p>
 * <p>Stabbing</p>
 * <p>Magic</p>
 *
 * @see player.armour.Defence
 */
public enum ArmingType {
    /** cutting weapons like clubs, hammer, ... */
    CUTTING,

    /** stabbing weapons like daggers, bullets, ... */
    STABBING,

    /** magic weapons including arrows */
    MAGIC
}
